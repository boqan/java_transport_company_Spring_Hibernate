package com.batanasov.javaTransportCompanyProjectSPRING.Enums;

/**
 * Enum representing different qualifications for employees.
 * Each enum constant corresponds to a specific qualification required for handling certain types of transport contracts.
 */
public enum Qualification {
    SPECIAL_CARGO,      // Qualification for transporting special cargo
    PASSENGER_TRANSPORT, // Qualification for transporting passengers
    HAZARDOUS_MATERIAL, // Qualification for transporting hazardous materials
    HEAVY_VEHICLES      // Qualification for driving heavy vehicles like trucks
}
